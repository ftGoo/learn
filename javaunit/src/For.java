public class For {
    /**
     * for循环验证
     */
    public static void main(String[] args) {
        int index = 0;
        for(index = 0;  index < 10 ; index++){

        }
        //测试打印出来是否为10，结果为10证明，index++执行完后不满足条件for循环不再执行，但是index++已经执行完毕
        System.out.println(index);
    }
}
